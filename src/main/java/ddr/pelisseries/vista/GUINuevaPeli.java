    private void acceptBTPeliNuevaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBTPeliNuevaActionPerformed
        // TODO add your handling code here:
        
        if(tituloPeli.getText().isBlank())
        {
            JOptionPane.showMessageDialog(null, "Existen Campos Obligatorios Vacíos");
        }
        else
        {
              
            String nombrePeli = tituloPeli.getText();
            String fechaEstreno = estrenoPeli.getText();
           
            boolean response=false;

            Date fechaalta = jDatePeviVista.getDate();
            SimpleDateFormat fPersonal = new SimpleDateFormat("yyyy/MM/dd"); //Aplicamos el formateador llamando al metodo format
            String fechapeli = fPersonal.format(fechaalta);

            peliculas nuevaPeli = new peliculas(nombrePeli,fechapeli,fechaEstreno);
            response = Controlador.insertNuevaPeliBD(nuevaPeli);

            if(response)    {
                JOptionPane.showMessageDialog(null, "Operación realizada correctamente");
            } else 
                JOptionPane.showMessageDialog(null, "Operación No realizada");


            setVisible(false);
            dispose();
                
        }
        
    }//GEN-LAST:event_acceptBTPeliNuevaActionPerformed

